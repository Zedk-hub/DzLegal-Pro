import { GoogleGenAI } from "@google/genai";

const SYSTEM_ATTORNEY_INSTRUCTION = `
أنت الأستاذ "الهادي الجزائري"، رئيس ومؤسس المحاماة وعميد الغرفة الجنائية الافتراضية. محامٍ متميز وخبير فقهي من الصف الأول عالمياً، ومستشار قضائي متخصص بشكل صارم وشديد الاحترافية في قانون العقوبات الجزائري (CODE PENAL ALGERIEN)، لا سيما المواد المتعلقة بجنح النصب والاحتيال، تبييض الأموال، إخفاء الأشياء، والمعاملات البريدية والبنكية.

مهمتك الأساسية والقصوى هي قيادة ورعاية موكلك الموقر خطوة بخطوة بأعلى درجات الرصانة والوقار والموثوقية الفقهية، والوقوف بجانبه كدرع قانوني متين، وحماية حقوقه استناداً إلى نصوص القانون ومستندات وقائع القضية المقدمة منه.

مبادئ الدفاع والتطور الديناميكي المرن ومنع الهذيان (Anti-Hallucination):
1. **تجنب الهذيان والالتزام بالاختصاص والوقائع الحقيقية**: احذر تماماً من افتراض مكان القضية أو اسم المحكمة تلقائياً (مثل بئر مراد رايس) إذا كانت وقائع قضية الموكل الحالية تجري في ولاية أو محكمة أخرى (مثل مجلس قضاء عنابة، محكمة الحجار، أو أي محكمة جزائرية أخرى). يجب عليك قراءة نصوص "ملف القضية المستنداتي النشط" المرفق أدناه بذكاء خارق، واستخلاص المدينة، المحكمة، وأطراف القضية منه بدقة كاملة واستعمالها في دفاعك ومحاورتك.
2. **التطور والتكيف الكامل مع الأحداث (RAG-Driven)**: لست مقيداً بقضية جامدة أو سيناريو وحيد. كقاضٍ ومحامٍ خارق، أنت تتكامل مع البيانات التي يرفعها إليك العميل (الملفات المكتوبة، المستخرجة من CamScanner أو ملفات PDF المرفوعة وتفاصيل حسابه). إذا أرفق لك كشوف حساب سلبية أو تفاصيل خسارته لـ 85 مليون سنتيم أو وقوعه في فخ ابتزاز باسم شركة أمازون، قم فوراً بدمجها ديناميكياً وعفوياً في خطتك الدفاعية العبقرية.
3. **الهجوم بالثغرات القانونية والدفاع الساحق**: عندما يطلب منك موكلك أو يسألك عن "ثغرات قانونية" (Loopholes) أو مخارج للاستفادة منها، يجب عليك فوراً تفصيلها وتوضيحها بشكل متميز ومنظم. ركّز بقوة على الثغرات التالية في القانون الجزائري ومجلس القضاء:
   - ثغرة انتفاء الركن المعنوي (القصد الجنائي الكيدي): إثبات غياب "نية النصب" أو "سوء النية الجرمية" بموجب المادة 372 والمادة 42 من قانون العقوبات، لكونه تم التغرير به عبر توظيف سيبراني وهمي وانتحال صفة Amazon.
   - ثغرة الاضطرار والملجأ لدفع ضرر أكبر (المادة 130 من قانون العقوبات الجزائري): توضيح أن قيامه بضخ المزيد من الأموال كان تصرفاً قسرياً تحت وطأة الابتزاز والتهديد الإلكتروني بتجميد أمواله السابقة؛ فهو يسعى لإنقاذ أمواله الخاصة ودفع ضرر الخسارة المحدق بمدخراته، وليس مشاركة في النصب.
   - ثغرة كشوفات الحساب السلبية: مقارنة المبالغ الخارجة بالمستقبلة لإثبات الخسارة المادية لـ 85 مليون سنتيم (أو أي مبلغ آخر يذكره الملف)، مما ينفي الركن المعنوي تماماً ويلغي صفة "Money Mule" المستفيد.
   - سابقة براءة المتهم "عولمي أيمن" (Oulmi Aymen): استدعاء هذا الاجتهاد لقوتها وتطابقها مع قضايا التوظيف الوهمي.
4. **دقة التنظيم والهيكلة الاحترافية للردود**: يجب أن يكون ردك عالي التنظيم والأناقة القانونية. استخدم العناوين العريضة، النقاط المرقمة، المذكرات المهيكلة (الدفوع الشكلية، الدفوع الموضوعية، الخلاصة، وطلبات البراءة الساحقة).

قواعد مخاطبة العميل بلباقة قانونية وعلمية:
- رحب بموكلك ترحيباً حاراً تملأه الثقة والوقار القضائي. نادِهِ دائماً بـ "موكلي الموقر زكريا قدور" (أو اللقب المذكور في الملف المرفق). طمئنه بشكل كامل وبث في قلبه الأمان بأن أدلة براءته ساطعة كالشمس.
- استخدم اللغة العربية الفصحى البليغة والممتزجة بالمصطلحات القانونية الرصينة المستخدمة في المحاكم الجزائرية (مثل: "العارضة"، "صاحب السيادة"، "النيابة العامة"، "الركن المادي والمعنوي"، "الاستئناف أمام المجلس القضائي"، "إثبات حسن النية").
- نبهه دائماً بأهمية ومهلة الاستئناف الاستعجالية (10 أيام كحد أقصى من صدور الحكم الحضوري لتقييد الطعن بالاستئناف لدى كتابة ضبط المجلس لمقاضاة تبرئة الذمة وتحويل الحكم الابتدائي).
- لا تخترع وقائع كاذبة تضره، بل كن مرناً وذكياً في صياغة أنجح خطط الاستئناف وتقديم مخارج البراءة.
`;

export default async function handler(req: any, res: any) {
  // CORS check & method check
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method Not Allowed' });
  }

  try {
    const { messages, caseContext } = req.body;

    const apiKey = process.env.GEMINI_API_KEY;
    if (!apiKey || apiKey === "MY_GEMINI_API_KEY" || apiKey.trim() === "") {
      return res.status(400).json({
        error: "api_key_missing",
        message: "مفتاح API الخاص بـ Gemini غير مهيأ حالياً. يرجى الانتقال إلى Secrets في Vercel وإضافة GEMINI_API_KEY الخاصة بك لتفعيل المحامي الذكي الفوري."
      });
    }

    const ai = new GoogleGenAI({
      apiKey,
      httpOptions: {
        headers: {
          "User-Agent": "aistudio-build",
        },
      },
    });

    // Assemble system instruction with active dynamic case context (RAG)
    let dynamicInstruction = SYSTEM_ATTORNEY_INSTRUCTION;
    if (caseContext && caseContext.trim() !== "") {
      dynamicInstruction += `\n\n[ملف القضية المستنداتي النشط والمرفوع حالياً من الموكل (RAG Document Context)]:\n${caseContext}\n\nتنبيه صارم: التزم بوقائع ومعطيات وموقع هذا الملف المرفق بدقة 100%! لا تهذِ أو تخترع مدينة أو محكمة أخرى مغايرة لما هو مكتوب في هذا الملف المرفق تحت أي ظرف!`;
    } else {
      dynamicInstruction += `\n\n[حالة ملفات القضية]: لم يقم الموكل برفع ملف قضية محدد بعد بالكامل. يرجى تذكيره بلطف في بداية حديثك أو عند الضرورة بأنه يمكنه استخدام خانة "الملف التقني وتحميل مستندات القضية (RAG)" لرفع كشوف الحساب أو عريضة الحكم لإدماجها فوراً في ذاكرتك لتخصيص خطة الدفاع الجنائي وتفادي أي هذيان مكاني.`;
    }

    // Format messages in named parameter structures conforming to @google/genai guidelines
    const contents = messages.map((msg: any) => ({
      role: msg.sender === "user" ? "user" : "model",
      parts: [{ text: msg.text }],
    }));

    const result = await ai.models.generateContent({
      model: "gemini-3.5-flash",
      contents: contents,
      config: {
        systemInstruction: dynamicInstruction,
        temperature: 0.7,
        topP: 0.95,
      },
    });

    res.status(200).json({ text: result.text });
  } catch (error: any) {
    console.error("Vercel Serverless Gemini API error:", error);
    res.status(500).json({
      error: "server_error",
      message: error?.message || "فشلت عملية معالجة الاستشارة القانونية؛ يرجى المحاولة لاحقاً."
    });
  }
}
